    <h2>Ejercicio 1</h2>
    <p>Determina cuál de las siguientes variables son válidas y explica por qué:</p>
    <p>$_myvar, $_7var, myvar, $myvar, $var7, $_element1, $house*5</p>
    <h4>Respuesta:</h4>
    <ul>
        <li>$_myvar es válida porque inicia con guión bajo.</li>
        <li>$_7var es válida porque inicia con guión bajo.</li>
        <li>myvar es inválida porque no tiene el signo de dolar ($).</li>
        <li>$myvar es válida porque inicia con una letra.</li>
        <li>$var7 es válida porque inicia con una letra.</li>
        <li>$_element1 es válida porque inicia con guión bajo.</li>
        <li>$house*5 es inválida porque el símbolo * no está permitido.</li>
    </ul>

    <h2>Ejercicio 2</h2>
    <p>Proporcionar los valores de $a, $b, $c como sigue:</p>
    <p>$a = "ManejadorSQL";<br />
       $b = 'MySQL';<br />
       $c = &amp;$a;</p>
    <h4>Valores de las variables:</h4>
    <ul>
        <li>$a = ManejadorSQL</li>
        <li>$b = MySQL</li>
        <li>$c = ManejadorSQL</li>
    </ul>

    <h3>Segunda asignación de valores</h3>
    <p>$a = "PHP server";<br />
       $b = &amp;$a;</p>
    <h4>Valores de las variables después de la segunda asignación:</h4>
    <ul>
        <li>$a = PHP server</li>
        <li>$b = PHP server</li>
        <li>$c = PHP server</li>
    </ul>
    <h3>Explicación</h3>
    <p>En la segunda asignación, la variable $b ahora es una referencia a $a, por lo que cualquier cambio en $a también se refleja en $b. Sin embargo, $c sigue apuntando a su asignación original de $a, por lo que también cambia cuando $a se modifica.</p>

    <h2>Ejercicio 3</h2>
    <p>Muestra el contenido de cada variable inmediatamente después de cada asignación, verificar la evolución del tipo de estas variables (imprime todos los componentes de los arreglos).</p>
    <p>Después de asignar $a = 'PHP5': $a = PHP5</p>
    <p>Después de asignar $z[] = &amp;$a: $z[0] = PHP5</p>
    <p>Después de asignar $b = '5a version de PHP': $b = 5a version de PHP</p>
    <p>Después de asignar $c = intval($b) * 10: $c = 50</p>
    <p>Después de asignar $a .= $b: $a = 5050</p>
    <p>Después de asignar $b *= $c: $b = 25502500</p>
    <p>Después de asignar $z[0] = 'MySQL': $z[0] = MySQL</p>
    <h3>Explicación</h3>
    <p>La variable $a inicialmente contenía "PHP5", y al asignarle una referencia en $z[0], cualquier cambio en $a se reflejaba en $z[0]. Cuando $a se concatenó con $b, su valor cambió, lo que afectó también a $z[0]. Posteriormente, al reasignar "MySQL" a $z[0], la referencia entre $a y $z[0] se rompió, por lo que ya no reflejan el mismo valor.</p>

