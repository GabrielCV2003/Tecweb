    <h2>Ejercicio 7</h2>
    <p>Usando la variable predefinida $_SERVER, determina lo siguiente:</p>
    <ul>
        <li>a. La versión de Apache y PHP</li>
        <li>b. El nombre del sistema operativo (servidor)</li>
        <li>c. El idioma del navegador (cliente)</li>
    </ul>
    <h4>Resultado de $_SERVER:</h4>
    <ul>
        <li>Versión de Apache: Apache/2.4.58 (Win64) OpenSSL/3.1.3 PHP/8.2.12</li>
        <li>Versión de PHP: 8.2.12</li>
        <li>Sistema operativo del servidor: Windows NT VALERIO 10.0 build 22631 (Windows 11) AMD64</li>
        <li>Idioma del navegador (cliente): es-ES,es;q=0.8,en-US;q=0.5,en;q=0.3</li>
    </ul>
    <h3>Explicación</h3>
    <p>La variable predefinida $_SERVER nos permite obtener información sobre el servidor y el cliente. En este caso, usamos:</p>
    <ul>
        <li><b>$_SERVER['SERVER_SOFTWARE']</b> para obtener la versión del servidor web (Apache).</li>
        <li><b>phpversion()</b> para obtener la versión de PHP instalada en el servidor.</li>
        <li><b>php_uname()</b> para obtener información sobre el sistema operativo del servidor.</li>
        <li><b>$_SERVER['HTTP_ACCEPT_LANGUAGE']</b> para obtener el idioma preferido del cliente según la configuración de su navegador.</li>
    </ul>

